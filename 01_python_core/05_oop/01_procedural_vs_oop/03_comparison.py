"""
对比总结：面向过程 vs 面向对象

对应文档: 06-object-oriented-programming.md § 6.1.3
"""

def print_comparison():
    summary = """
    +----------------+--------------------------+--------------------------+
    | 特性           | 面向过程 (POP)           | 面向对象 (OOP)           |
    +----------------+--------------------------+--------------------------+
    | 核心           | 函数 (Function)          | 对象 (Object)            |
    | 关注点         | 怎么做 (How/Process)     | 是什么 (What/Entity)     |
    | 数据和操作     | 分离 (Global Data + Func)| 封装 (Class + Method)    |
    | 代码复用       | 函数调用                 | 继承、多态               |
    | 扩展性         | 较差                     | 较好                     |
    | 维护性         | 随规模增大而变难         | 易于维护                 |
    | 适用场景       | 脚本、简单逻辑、底层开发 | 大型应用、复杂业务逻辑   |
    +----------------+--------------------------+--------------------------+
    """
    print(summary)

if __name__ == '__main__':
    print_comparison()
