# 定义函数使用 def 关键字。
# 基本语法：
# def 函数名(参数列表):
#     """函数文档字符串（可选）"""
#     函数体
#     return 返回值（可选）

# 语法规则：
# - 使用 def 关键字开始函数定义
# - 函数名遵循标识符命名规则（小写字母+下划线）
# - 参数列表放在括号内，可以为空
# - 函数体必须缩进（通常 4 个空格）
# - return 语句可选，用于返回值

def my_function():
    """这是一个最简单的函数示例"""
    print("Executing my_function")

if __name__ == "__main__":
    my_function()
