# 运算符优先级从高到低：
# 1. 括号：()
# 2. 幂运算：**
# 3. 正负号：+x, -x, ~x
# 4. 乘除取余：*, /, //, %
# 5. 加减：+, -
# 6. 位移：<<, >>
# 7. 按位与：&
# 8. 按位异或：^
# 9. 按位或：|
# 10. 比较运算：<, <=, >, >=, ==, !=
# 11. 身份运算：is, is not
# 12. 成员运算：in, not in
# 13. 逻辑非：not
# 14. 逻辑与：and
# 15. 逻辑或：or

# 优先级示例
result1 = 2 + 3 * 4
print(f"2 + 3 * 4 = {result1}")  # 先乘后加: 14

result2 = (2 + 3) * 4
print(f"(2 + 3) * 4 = {result2}")  # 括号优先: 20

# 建议使用括号提高可读性
a, b, c, d = 1, 2, 3, 4
result3 = (a > b) and (c < d)
print(f"(a > b) and (c < d): {result3}")
