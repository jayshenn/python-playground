# 用于对二进制位进行操作。

a = 60  # 二进制: 0011 1100
b = 13  # 二进制: 0000 1101

print(f"a = {a} ({bin(a)})")
print(f"b = {b} ({bin(b)})")

print(f"a & b = {a & b} ({bin(a & b)})")   # 按位与: 12 (0000 1100)
print(f"a | b = {a | b} ({bin(a | b)})")   # 按位或: 61 (0011 1101)
print(f"a ^ b = {a ^ b} ({bin(a ^ b)})")   # 按位异或: 49 (0011 0001)
print(f"~a = {~a} ({bin(~a)})")      # 按位取反: -61
print(f"a << 2 = {a << 2} ({bin(a << 2)})")  # 左移: 240 (1111 0000)
print(f"a >> 2 = {a >> 2} ({bin(a >> 2)})")  # 右移: 15 (0000 1111)

# 实际应用：快速计算
# 左移1位相当于乘以2
print(f"5 << 1 = {5 << 1}")  # 10

# 右移1位相当于除以2
print(f"10 >> 1 = {10 >> 1}")  # 5
